    private boolean endsWithNumber() {
        String str = jTextField1.getText();
        // Check if the string is empty or null
        if (str == null || str.isEmpty()) {
            return false;
        }

        // Iterate through the characters from right to left
        for (int i = str.length() - 1; i >= 0; i--) {
            char ch = str.charAt(i);
            // Check if the character is a digit
            if (Character.isDigit(ch)) {
                return true; // The string ends with a number
            } else if (!Character.isWhitespace(ch)) {
                // If the character is not a digit and not whitespace,
                // then the string does not end with a number
                return false;
            }
        }

        return false; // The string does not end with a number
    }

